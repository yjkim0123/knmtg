def dataset_statistics() -> dict:
    """Return node/edge counts for paper Table 1."""
    from collections import Counter
    all_coords = {**COORDS, **REGIONAL_COORDS, **INTERCITY_BUS_TERMINALS, **BRT_HUBS}
    nt = build_node_types()
    node_counts = Counter(nt.get(k, "subway") for k in all_coords)

    # Count unique undirected edge pairs from the routing graph
    import sys
    sys.path.insert(0, str(__file__).replace("seoul_subway_data.py", ""))
    try:
        from meeting_finder_v3 import build_graph
        G = build_graph()
        edge_pairs = set()
        for src, edges in G.items():
            for e in edges:
                edge_pairs.add(tuple(sorted([src, e.to])))
        total_edges = len(edge_pairs)
    except Exception:
        # fallback: sum raw source arrays
        total_edges = (
            sum(len(s) - 1 for s in LINE_SEGMENTS.values()) +
            sum(len(s) - 1 for s in MUGUNGHWA_SEGMENTS.values()) +
            sum(len(s) - 1 for s in SAEMAEUL_SEGMENTS.values()) +
            sum(len(s) - 1 for s in REGIONAL_SEGMENTS.values()) +
            len(KTX_SEGMENT_TIMES) + len(KTX_TRANSFERS) +
            len(GTX_A_SEGMENT_TIMES) + len(GTX_A_TRANSFERS) +
            len(INTERCITY_BUS_TIMES) + len(INTERCITY_BUS_STATION_LINKS) +
            len(BRT_ROUTE_TIMES) + len(BRT_HUB_LINKS) +
            len(REGIONAL_TRANSFERS) + len(ITX_MAEUM_DIRECT_TIMES)
        )

    return {
        "nodes": {
            "total": len(all_coords),
            **dict(node_counts),
        },
        "edges": {
            "total": total_edges,
        },
    }
