    if distance_km < 40:   return 3000
    if distance_km < 70:   return 4500
    if distance_km < 100:  return 5500
    if distance_km < 130:  return 7500
    if distance_km < 165:  return 10000
    if distance_km < 210:  return 13500
    if distance_km < 270:  return 17000
    if distance_km < 330:  return 21000
    return 24500


